#!/usr/bin/env node
/**
 * scripts/sprint-reporter.js
 * Phase 15: Sentient Infrastructure
 * 
 * Auto-generates a SPRINT_REPORT.md using 'docs-narrator' to analyze
 * git commits, test coverage, and PR merges over the last 7 days.
 */

const { exec } = require('child_process');
const path = require('path');

const PROJECT_DIR = process.env.HOME + '/.openclaw/workspace';
const OUT_FILE = path.join(PROJECT_DIR, 'SPRINT_REPORT.md');

console.log("📊 Compiling Sprint Metadata...");

// In a real scenario, this extracts raw `git log --since="7 days ago"` 
// and `vitest run --coverage` data, then pipes it to docs-narrator.

const command = `openclaw agent --agent docs-narrator --message "SYSTEM: Generate an executive SPRINT_REPORT.md based on the last 7 days of rig activity. Extract the git history of the current workspace, highlight squashed bugs, and note the VRAM saved by context compression."`;

exec(command, { cwd: PROJECT_DIR }, (err, stdout) => {
    if (err) {
        console.error("❌ Sprint Report generation failed:", err.message);
    } else {
        console.log(`✅ Sprint Report successfully generated by docs-narrator.`);
    }
});
